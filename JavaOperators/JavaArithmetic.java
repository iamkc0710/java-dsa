package JavaOperators;

public class JavaArithmetic {
    public static void main(String args[]){

    // Binary Arithmetic Operators
    int a = 10;
    int b = 5;
    
    System.out.println(a+b);
    System.out.println(a-b);
    System.out.println(a*b);
    System.out.println(a/b);
    System.out.println(a%b);

    
    // Unary Arithmetic Operators
    a++;
    b--;  // b = b-1

    System.out.println(a);
    System.out.println(b);


    }
}
