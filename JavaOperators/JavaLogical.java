package JavaOperators;

public class JavaLogical {
    
    public static void main(String[] args) {
        
    // Logical AND (&&)
    System.out.println((3>1) && (4>3));
    System.out.println((3>5) && (4>3));


    // Logical OR (||)
    System.out.println((3>1) || (4>3));
    System.out.println((3>5) || (4>3));

    // Logical NOT (!)
    System.out.println(!(4>3));
    System.out.println(!(3>5));


    }
}
